export function getEventsByYear(year: number): Event[] {
  switch (year) {
    case 2026:
      return events2026;
    case 2025:
      return events2025;
    default:
      return events2026;
  }
}

export function getEventById(id: string, year: number): Event | undefined {
  const events = getEventsByYear(year);
  return events.find((event) => event.id === id);
}
