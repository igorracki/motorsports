export interface SessionResult {
  position: number;
  driver: string;
  team: string;
  time: string;
  gap: string;
}

export interface Session {
  code: string;
  name: string;
  startTime: string;
  results?: SessionResult[];
}

export interface TrackStats {
  length: string;
  corners: number;
  drsZones: number;
  lapRecord: string;
  lapRecordHolder: string;
  lapRecordYear: number;
}

export interface Event {
  id: string;
  title: string;
  country: string;
  countryCode: string;
  location: string;
  trackName: string;
  dateFrom: string;
  dateTo: string;
  sessions: Session[];
  trackStats: TrackStats;
  status: "completed" | "ongoing" | "upcoming";
}

// Helper to get session codes for card display
export function getSessionCodes(sessions: Session[]): string[] {
  return sessions.map((s) => s.code);
}

